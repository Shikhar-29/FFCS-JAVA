    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if(row != -1) {
            jDialog1.setVisible(true);
            jLabel1.setText("");
            String id = sendReg();
            if(row != -1) {
                String selCourse = jTable1.getModel().getValueAt(row, 0).toString();
                String facultyID = jTable1.getModel().getValueAt(row, 2).toString();
                ConnectDB connectObj = new ConnectDB();
                Connection c1 = connectObj.connecttoDB();
                if(c1 != null) {
                    DBQueryExecution obj1 = new DBQueryExecution();
                    try {
                        ResultSet rs1 = obj1.viewMarks(c1, selCourse + "_" + facultyID, id);
                        if(rs1 != null) {
                            if(!rs1.getBoolean("STATUS")) {
                                jTextField4.setText("Not Evaluated");
                                jTextField6.setText("---");
                            }
                            else {
                                jTextField4.setText("Evaluated");
                                jTextField6.setText(rs1.getString("MARKS"));
                            }
                        }
                    } catch(SQLException ex) {
                        System.out.println("Some error occured !!");
                    }
                }
            }
        }
        else
        jLabel1.setText("No Course Selected. Select a Course !!");
    }//GEN-LAST:event_jButton2MouseClicked

