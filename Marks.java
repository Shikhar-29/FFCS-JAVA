     public void setTableData() {
        jTable1.clearSelection();
        String id = sendReg();
        ConnectDB connectObj = new ConnectDB();
        Connection c1 = connectObj.connecttoDB();
        ResultSet rs1,rs2;
        if(c1 != null) {
            DBQueryExecution obj1 = new DBQueryExecution();
            rs1 = obj1.viewCourses_Attendance(c1, id);
            rs2 = obj1.viewCourses_Attendance(c1, id);
            try {
                int size = 1;
                while(rs1.next()) {
                    size++;
                }
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(size);
                jTable1.setModel(dtm);
                for(int i=0;i<size;i++){
                    jTable1.setValueAt(rs2.getString("COURSE_CODE"), i, 0);
                    jTable1.setValueAt(rs2.getString("COURSE_NAME"), i, 1);
                    jTable1.setValueAt(rs2.getString("FACULTYID"), i, 2);
                    if(rs2.next()) {
                    } else {
                        break;
                    }
                }
            } catch (SQLException | NullPointerException ex) {
                System.out.println("Some Error occured !!");
            }
        }
    }
